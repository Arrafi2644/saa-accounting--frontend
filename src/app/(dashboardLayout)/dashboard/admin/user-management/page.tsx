
"use client";

import DashboardManagementPageSkeleton from "@/components/modules/dashboard/DashboardManagePageSkeleton";
import UserToolbar from "@/components/modules/dashboard/user/UserToolbar";
import { useGetAllUsersQuery, useDeleteUserMutation } from "@/redux/features/user/user.api";
import { IUser } from "@/types";
import { ColumnDef } from "@tanstack/react-table";
import DashboardPageHeader from "@/components/modules/dashboard/DashboardPageHeader";
import { toast } from "sonner";

import UserDetailsModal from "@/components/modules/dashboard/user/UserDetailsModal";
import UpdateUserModal from "@/components/modules/dashboard/user/UpdateUserModal";
import DeleteAlert from "@/components/modules/dashboard/DeleteAlert";
import React from "react";
import { DynamicDataTable } from "@/components/modules/dashboard/DataTable";

const UserManagementPage = () => {
  const [deleteUser] = useDeleteUserMutation();

  const [searchTerm, setSearchTerm] = React.useState("");
  const [sort, setSort] = React.useState("");

  const { data, isLoading, isError } = useGetAllUsersQuery({
    ...(searchTerm && { searchTerm }),
    ...(sort && { sort })
  });
  // Modal states
  const [selectedUser, setSelectedUser] = React.useState<IUser | null>(null);
  const [openViewModal, setOpenViewModal] = React.useState(false);
  const [userToUpdate, setUserToUpdate] = React.useState<IUser | null>(null);
  const [openUpdateModal, setOpenUpdateModal] = React.useState(false);
  const [userToDelete, setUserToDelete] = React.useState<IUser | null>(null);
  const [openDeleteAlert, setOpenDeleteAlert] = React.useState(false);

  // Delete handler
  const handleDelete = async (user: IUser) => {
    try {
      const res = await deleteUser(user._id);
      if (res?.data?.success) {
        toast.success("User deleted successfully");
      }
    } catch (error) {
      toast.error("Failed to delete user");
    }
  };

  // Columns
  const columns: ColumnDef<IUser>[] = [
    { accessorKey: "name", header: "Name" },
    { accessorKey: "email", header: "Email" },
    { accessorKey: "role", header: "Role" },
  ];

  // Actions
  const actions = [
    {
      label: "View",
      onClick: (user: IUser) => {
        setSelectedUser(user);
        setOpenViewModal(true);
      },
    },
    {
      label: "Edit",
      onClick: (user: IUser) => {
        setUserToUpdate(user);
        setOpenUpdateModal(true);
      },
    },
    {
      label: "Delete",
      onClick: (user: IUser) => {
        setUserToDelete(user);
        setOpenDeleteAlert(true);
      },
    },
  ];

  <DynamicDataTable columns={columns} data={data?.data ?? []} actions={actions} />


  if (isLoading) return <DashboardManagementPageSkeleton />;
  if (isError) return <p>Error loading users.</p>;

  return (
    <div>
      <DashboardPageHeader title="User Management" />
      {/* <UserToolbar
        onSearchChange={setSearchTerm}
        onSortChange={setSort}
      /> */}
      <UserToolbar
  onSearchChange={setSearchTerm}
  onSortChange={setSort}
/>
      <DynamicDataTable columns={columns} data={data?.data ?? []} actions={actions} />

      {/* View Modal */}
      {selectedUser && (
        <UserDetailsModal
          open={openViewModal}
          onOpenChange={setOpenViewModal}
          user={selectedUser}
        />
      )}

      {/* Update Modal */}
      {userToUpdate && (
        <UpdateUserModal
          open={openUpdateModal}
          onOpenChange={setOpenUpdateModal}
          user={userToUpdate}
        />
      )}

      {/* Delete Confirmation */}
      {userToDelete && (
        <DeleteAlert
          open={openDeleteAlert}
          onOpenChange={setOpenDeleteAlert}
          description={`Are you sure you want to delete ${userToDelete.name}? This action is permanent and cannot be undone.`}
          onConfirm={async () => {
            await handleDelete(userToDelete);
            setOpenDeleteAlert(false);
            setUserToDelete(null);
          }}
        />
      )}
    </div>
  );
};

export default UserManagementPage;
